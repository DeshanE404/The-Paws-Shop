     private void addHintTextToTextField() {
        // Set initial hint text
        jTextField1.setText("Enter Product ID");
        jTextField1.setForeground(java.awt.Color.GRAY);

        // Add focus listener for handling hint text
        jTextField1.addFocusListener(new java.awt.event.FocusListener() {
            @Override
            public void focusGained(java.awt.event.FocusEvent e) {
                if (jTextField1.getText().equals("Enter Product ID")) {
                    jTextField1.setText("");
                    jTextField1.setForeground(java.awt.Color.BLACK);
                }
            }

            @Override
            public void focusLost(java.awt.event.FocusEvent e) {
                if (jTextField1.getText().isEmpty()) {
                    jTextField1.setText("Enter Product ID");
                    jTextField1.setForeground(java.awt.Color.GRAY);
                }
            }
        });
    }
private void loadallproduct(){
    DefaultTableModel model = (DefaultTableModel )jTable1.getModel();
    model.setRowCount(0);
      try (Connection conn = Database.getConnection()) {
            String query = "SELECT * FROM petsupply";
            try (PreparedStatement pst = conn.prepareStatement(query);
                 ResultSet rs = pst.executeQuery()) {

                while (rs.next()) {
                    model.addRow(new Object[]{
                        rs.getString("ProductID"),
                        rs.getString("Name"),
                        rs.getString("Availability"),
                        rs.getString("Price"),
                        rs.getString("Size"),
                        rs.getString("Colour"),
                        rs.getString("ProductType"),
                        rs.getString("Description")
                    });
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error loading data: " + ex.getMessage(),
                    "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }

