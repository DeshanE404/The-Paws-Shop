    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
String productID = jTextField1.getText().trim();
 if (productID.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a Product ID!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try (Connection conn = Database.getConnection()) {
            String query = "DELETE FROM petsupply WHERE ProductID = ?";
            try (PreparedStatement pst = conn.prepareStatement(query)) {
                pst.setString(1, productID);
                int rowsAffected = pst.executeUpdate();

                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, "Record deleted successfully!");
                    loadallproduct();
                } else {
                    JOptionPane.showMessageDialog(this, "No record found with the given Product ID!", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }       // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

